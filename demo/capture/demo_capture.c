static VOID appCapture(void)
{
	BOOL ret;
	T_AMOPENAT_CAM_CAPTURE_PARAM cameraparam;
	cameraparam.imageWidth = CAM_SENSOR_WIDTH;
	cameraparam.imageHeight = CAM_SENSOR_HEIGHT;
	cameraparam.quality = 100;
	ret = iot_camera_capture("cap.jpg", &cameraparam);

	if (ret)
	{
		iot_debug_print("camera_capture size %d", iot_fs_file_size("cap.jpg"));
	}
	iot_debug_print("camera_capture %d",ret);
}


static BOOL appCameraInit(void)
{
	BOOL ret;
	T_AMOPENAT_CAMERA_PARAM initParam =
	{
		NULL,
		OPENAT_I2C_1, 
		CAM_SPI_SLAVE_ADDR,
		AMOPENAT_CAMERA_REG_ADDR_8BITS|AMOPENAT_CAMERA_REG_DATA_8BITS,

		TRUE,
		TRUE,
		TRUE, 

		CAM_SENSOR_WIDTH,
		CAM_SENSOR_HEIGHT,
		CAMERA_IMAGE_FORMAT_YUV422,
		cameraInitReg,
        sizeof(cameraInitReg)/sizeof(AMOPENAT_CAMERA_REG),
		{CAM_ID_REG, CAM_ID_VALUE},
		{2,3,TRUE},
		1,
		OPENAT_SPI_MODE_MASTER2_2,
		OPENAT_SPI_OUT_Y1_V0_Y0_U0,
		FALSE,
		CAM_SPI_SPEED
	};

	ret = iot_camera_init(&initParam);
	if (!ret)
		return FALSE;
	
	ret = iot_camera_poweron(FALSE);
	if (!ret)
		return FALSE;

	return TRUE;
}

int appimg_enter(void *param)
{    
	iot_debug_print("[capture] appimg_enter");

	appCameraInit();

	appCapture();
	
    return 0;
}

void appimg_exit(void)
{
    iot_debug_print("[capture] appimg_exit");
}

